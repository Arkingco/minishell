#include "libft.h"

char		*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*d;
	unsigned int	i;

	if (s == NULL)
		return (NULL);
	d = (char *)malloc(sizeof(char) * (ft_strlen((char *)s) + 1));
	if (d == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		d[i] = f(i, s[i]);
		i++;
	}
	d[i] = '\0';
	return (d);
}
